# Lists : Python lists are container to store a set of values of any data type
# eg. fruits = ["Apple", "Mango", "Guava", 7, True] 

data = [1, 2, 4, 56, 43] #create a list
print(data) #print the list
print(data[2]) #access list element
data[0] = 90 #changing index value
print(data)

#creating a list using items of different types
a = ["Apple", 15, True, 'A']
print(a)

#List slicing
friends = ["Arif", "Pallavi", "Afreen", "Manini"]
print(friends[0:3])