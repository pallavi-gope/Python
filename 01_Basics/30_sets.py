# Sets : sets is a collection of non-repetetive elements.
# Properties of set - 
#1. Sets are unordered
#2. Sets are unindexed - cannot access by it's index
#3. There is no way to change it's items in a set
#4. Sets cannot contain duplicate values

#creating a set
a = {1, 3, 4, 5, 1, 1}

#printing the set
print(a) #removes the repetetive elements
print(type(a)) # default type is set

