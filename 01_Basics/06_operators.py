a = 10
b = 2

# Arithmetic operators (+, -, *, /)
print("Addition : ", a+b )
print("Subtraction : ", a-b )
print("Multiplication : ", a*b )
print("Division : ", a/b )

# Assignment operators (+=, -=, *=, /=)
a = 34
b = 20
c = 30
d = 32

a += 2
b -= 2
c *= 2
d /= 2

print("Assignment + operator : ",a)
print("Assignment - operator : ",b)
print("Assignment * operator : ",c)
print("Assignment / operator : ",d)

# Comparison operators (>, <, >=, <=, ==, !=)
x = 4
y = 7

a = (x>y)
b = (x<y)
c = (x>=y)
d = (x<=y)
e = (x==y)
f = (x!=y)

print("x is greater than y : ",a)
print("x is less than y : ",b)
print("x is greater than or equal to y : ",c)
print("x is less than or equal to y : ",d)
print("x is equal to y : ",e)
print("x is not equal to y : ",f)

#Logical operators (and, or, not)
a = True
b = False

print("a and b", a and b)
print("a or b", a or b)
print("not a", not a) #prints opposite

