#escape sequence characters - 

# 1. \n : to go to new line
story = "Python is a great language.\nEasy to understand and learn."
print(story)

# 2. \t : inserts the tab in a string 
story = "Python is a great language.\tEasy to understand and learn."
print(story)

# 3. \', \\, \" : inserts the single quote, slash, double quote inside a string
story = 'Python is a great language.\'Easy to understand and learn.\''
print(story)