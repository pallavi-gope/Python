#Loops : Sometimes we want to repeat a set of statements in our program, loops make it easier to tell the computer, which set of instruction to repeat and how.

## Types of Loop: Primarily there are two types of loops in python.

#1. for loop : for loop is used to iterate through a sequece like list, tuple or string.
#2. while loop :  In while loop the condition is checked first. If it evaluates true, the body of the loop executed otherwise exit the loop.  

#Note : If the condition never become false, the loop will keeps executing and will create the infinite loop.
